<script lang="ts">
	import { onMount } from "svelte";
	import { goto } from "$app/navigation";
	import supabase from "$lib/supabaseClient";

	onMount(async () => {
		const {
			data: { session }
		} = await supabase.auth.getSession();

		if (session) {
			goto("/jobs");
		} else {
			goto("/log-in");
		}
	});
</script>

<p>Checking authentication...</p>
